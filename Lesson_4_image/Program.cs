void Print_image(int[,] image)
{
    for (int i = 0; i < image.GetLength(0); i++)
    {
        for (int j = 0; j < image.GetLength(1); j++)
        {
            // Console.WriteLine($"{image[i, j]} ");
            if (image[i, j] == 0) Console.Write(" ");
            else Console.Write($"+");
        }
        Console.WriteLine();
    }
}

void Fill_Image(int row, int col)
{
    if (pic[row,col] == 0)
    {
        pic[row,col] = 1;
        Fill_Image(row - 1, col);
        Fill_Image(row, col - 1);
        Fill_Image(row + 1, col);
        Fill_Image(row, col + 1);
    }
}

